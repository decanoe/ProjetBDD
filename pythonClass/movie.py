class Movie:
    def __init__(self,id, title, realisator, date, duration, image_path, genres, resum, resum_author):
        self.id = id
        self.title = title
        self.realisator = realisator
        self.date = date
        self.duration = duration
        self.image_path = image_path
        self.genres = genres
        self.resum = resum
        self.resum_author = resum_author